package Mylib;
public class PhepTinh {
    public int cong(int a,int b)
    { return a+b; }
    public int tru(int a,int b)
    { return a-b; }
    public int nhan(int a,int b)
    { return a*b; }
    public int chianguyen(int a,int b)
    { return a/b; }
    public int chiadu(int a,int b)
    { return a%b;}
}